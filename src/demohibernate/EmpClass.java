package demohibernate;

public class EmpClass
{
   private int eno;
   private String ename;
   private int esal;

    public int getEno() {
        return eno;
    }

    public String getEname() {
        return ename;
    }

    public int getEsal() {
        return esal;
    }

    public void setEno(int eno) {
        this.eno = eno;
    }

    public void setEname(String ename) {
        this.ename = ename;
    }

    public void setEsal(int esal) {
        this.esal = esal;
    }
   
   
}
